#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst)
	{
		while (lst->next)
		{
			(f)(lst);
			lst = lst->next;
		}
		(f)(lst);
	}
}
